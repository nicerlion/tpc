									<div class="presell-four-copy">
										<p>The Hermes Birkin bag is the epitome of exclusivity and luxury. With rumored five year waiting lists and an insatiable appetite for these rare and elusive designer handbags, people have been willing to pay anywhere from $8,000 to $43,000. Recognized as the most expensive bag in the world and the hardest to obtain, Birkin illustrates the arbitrary pricing schemes that designer brands have been able to pull off. As luxury brands ranging from Fendi to Chanel know all too well, style knows no pricetag. As the saying goes, "if you have to ask, you can't afford it." But let's say we do ask. How much do designer bags really cost? The answer, you'll find, will make you cringe but perhaps more importantly, it will make you take a stand.</p>
										<p>Luxury brands begin pricing their handbags at the end. Though designer brands often say the high cost is due to the high quality materials sourced from Italy or France and upscale designs by the most artistic and skilled craftsman, the details of this are often an afterthought. Before the handbag is ever even made, before the materials are sourced and before the retailer is contacted, luxury brands decide their price based on what they think customers are willing to pay. From there, they decide their profit margins. For example, a gorgeous leather handbag may cost $2,000 to design and manufacture. The brand will then double the price to sell to the retailer for $4,000 who will again double the price for the customer. Upscale bags thus hold the highest markup in luxury fashion, often selling for 12 to 20 times the cost to make the bag.</p>
										<p>This pricing methodology, known as the <strong><a href="/perfect-purse-1/">keystone markup</a></strong>, is the central tenet to establishing status and maximizing a product's price. By holding supply hostage, luxury brands can gouge the customer even more by rendering their iconic brand priceless. As Fendi CEO Michael Burke has siad, "We have gone back to our elitist roots; our definition of luxury is the result of the human hand." By "human hand," it's important to clarify, he likely means human calculator. In a world where profit margins and mystery cloak the entire luxury designer bag industry, it is no wonder that you'll find these bags selling on ebay for $250,000 or more and capable of end-of-year discounts up to 70 percent. The marketing, not the craftmanship, are what make luxury so luxurious.</p>
										<div class="bold-light-yellow">
											<p>But in today's modern society,the definition of luxury is changing. Designer bags, once considered to be the crown jewel of fashion, are being flipped upside down. As consumers become wiser about the luxury hand bag supply chain and the exploitation of their own wallets, start-ups and previously unknown brands are making a resurgence. With e-commerce and the ability to speak to millions of people with a single tweet changing the name of the game, luxury designer brands are being forced to be more transparent. The once reliable markup as high as 20 times the cost to make a luxury handbag may no longer be sustainable and for consumers, this is finally the day of reckoning. It is the democratization of luxury, a time when all women deserve the opportunity to look and feel glamourous.</p>
										</div>
										<p>For many women, luxury purses are walking advertisements symbolizing financial success and status. High-end handbags like Bottega Veneta, Céline, Chanel and Hermés, which have been designed to be recognized, give their owners strength and confidence. Like a gorgeous dress or pair of heels, a well-known and well-made handbag makes a woman feel beautiful. And that is something worth fighting for, which is why one company is working to change the way we shop and acquire designer handbags.</p>
