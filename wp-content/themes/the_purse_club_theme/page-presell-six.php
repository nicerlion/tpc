										<p class="presell-six-caption">Creative Lab / Shutterstock.com<br> Luxury handbags in Prada store, in Milan, Italy.</p>
										<div class="presell-six-copy">
											<p>Fashion is fickle and no more so is this true than in high-end accessories. Trends come and go, lasting only a year or two, before a another one comes along tugging dollars in a new direction. The luxury handbag industry, however, has occupied an untouchable position in the fashion world. This segment of the fashion industry has largely dictated its own trends, and the dollars spent on them, by creating "it" bags, carefully crafting waiting lists, and controlling marketing, pricing, and manufacturing.</p>
											<p>Here are several surprising ways the top designers have had a stranglehold on the handbag industry, and why this grip is loosening, allowing exciting new talent to rise up and make their mark.</p>
											<h4>One-Source Manufacturing</h4>
											<p>A quick stroll down Fifth Avenue or Rodeo Drive reveals window after window of the latest must-have fashion trends designed to appeal to a woman's desire to make a fashion statement, especially when it comes to handbags. Touting the finest leather, attention to detail, and expert tooling by the world's greatest craftsmen, these luxury handbags are positioned as unique, one-of-a-kind purchases.</p>
											<p>Yet, a large number of these designer purses not only occupy neighboring retail windows, they are manufactured in one small town in Spain called Ubrique. According to BBC Business, workers in this unlikely enclave in southern Spain toil side by side making most of the leather products for high-end luxury brands like Louis Vuitton, Gucci, Hermes, Chanel, Chloe, Loewe, and Caroline Herrera.</p>
											<p>The production is conducted under a veil of secrecy for all workers, from laborers to factory heads, have to sign strict confidentiality contracts. This secrecy has also prevented lesser-known manufacturers from having access to these craftsmen, in effect allowing the big designers to corner the manufacturing market.</p>
											<h4>Market Manipulation</h4>
											<p>The top select brands in the designer world control over 75% of the $225 billion luxury fashion industry, meaning they've been operating unchallenged for years, determining pricing and supply. And, according to Forbes, upscale handbags have the highest markup in luxury goods, selling for 12 to 20 times the cost of production.</p>
											<p>As these top designer companies jockey for market share, they also routinely inflate prices, regardless of whether they align with inflation rates or production costs. As a result, these companies often artificially position themselves as luxury products</p>
											<p>And it has worked. The rise in sales of handbags in the United States alone over the last two decades has been seemingly unstoppable, peaking at $11.3 billion in 2015. But every juggernaut loses steam at some point, and designer purses are no exception. In the last five years, sales have gone from a growth rate of 16% to just 3% predicted for 2017</p>
										</div><!--ends presell-six-copy-->
