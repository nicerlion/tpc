										<p class="presell-six-caption">Creative Lab / Shutterstock.com<br>Renowned brands with prominent logos have fallen out of favor by the new generation, that is logo-adverse.</p>
										<div class="presell-six-copy">
											<h4>Oversaturation, the Death of Design, and Logo No-go</h4>
											<p>With the slowdown in handbag sales, big-name designers are fighting to maintain market share, at the cost of new design and innovation. In recent years, top handbag designers aren't spending as much on new designs, but rather trying to mass market their top sellers. They're offering the same product year after year, while inflating prices at the same time.</p>
											<p>Not only does this hurt the industry in terms of innovation, the market saturation works against these top designers as well. If women are all buying the same handbag, the cache of obtaining something unique and special evaporates quickly.</p>
											<p>All of this is compounded by the fact that the millennial generation, which is the largest generation the United States has ever seen, has less to spend on luxury goods thanks to the school debt they're saddled with before they even enter the workforce.</p>
											<p>This generation is also logo-adverse. According to a report conducted by Goldman Sachs, teenagers and young adults have a disdain for logos, forcing handbag companies like Coach, Kate Spade, and Michael Kors to tone down their branding. Because they've grown up in an age where they're relentlessly inundated with marketing messages, millennials love the idea of uniqueness and they don't like to be told what they should wear or what purse they should carry.</p>									
										</div>
